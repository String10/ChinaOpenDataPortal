package nju.websoft.chinaopendataportal.Bean;

public class Portal {
    String province;
    String city;
    String portal_url;
    String portal_name;

    public String getProvince() {
        return province;
    }

    public void setProvince(String province) {
        this.province = province;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getPortal_url() {
        return portal_url;
    }

    public void setPortal_url(String portal_url) {
        this.portal_url = portal_url;
    }

    public String getPortal_name() {
        return portal_name;
    }

    public void setPortal_name(String portal_name) {
        this.portal_name = portal_name;
    }
}
